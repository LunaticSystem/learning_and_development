// Allows us the run this as a standalone script
package main

// Importing the fmt standard library module for println
import "fmt"

// Main function
// Creates two variables day and night and assigns them as strings with values
func main() {
	// Static Variables
	var day string = "Sunny"
	var night string = "Dark"

	// Printing the contents in the PrintLn
	fmt.Println("during the day it is", day, "and when it is night time the sky is", night)
}
